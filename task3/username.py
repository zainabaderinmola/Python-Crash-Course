'''
This python script returns the user's full name
'''

first_name = input("Hello there! Please enter your first name: ")
print(f'You have entered your first name: {first_name}')

second_name = input("Hello there! Please input your second name: ")
print(f'You have entered your second name: {second_name}')

print(f"User's full name is {first_name} {second_name}")
